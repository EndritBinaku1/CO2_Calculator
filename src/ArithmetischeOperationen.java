public class ArithmetischeOperationen {
    public static void main(String[] args) {

        int c, e, g, h, l;
        int a = 5;
        int b = 10;

        c = a + b;
        e = b - a;
        g = a * b;
        h = b / a;
        l = a % b;

        System.out.println(c);
        System.out.println(e);
        System.out.println(g);
        System.out.println(h);
        System.out.println(l);



    }
}
